pub fn layout() -> KeyboardLayout {
    let mappings: Vec<(char, char)> = vec![
        // Lowercase QWERTY -> Russian
        ('q', '\u{0439}'), // й
        ('w', '\u{0446}'), // ц
        ('e', '\u{0443}'), // у
        ('r', '\u{043A}'), // к
        ('t', '\u{0435}'), // е
        ('y', '\u{043D}'), // н
        ('u', '\u{0433}'), // г
        ('i', '\u{0448}'), // ш
        ('o', '\u{0449}'), // щ
        ('p', '\u{0437}'), // з
        ('[', '\u{0445}'), // х
        (']', '\u{044A}'), // ъ
        ('a', '\u{0444}'), // ф
        ('s', '\u{044B}'), // ы
        ('d', '\u{0432}'), // в
        ('f', '\u{0430}'), // а
        ('g', '\u{043F}'), // п
        ('h', '\u{0440}'), // р
        ('j', '\u{043E}'), // о
        ('k', '\u{043B}'), // л
        ('l', '\u{0434}'), // д
        (';', '\u{0436}'), // ж
        ('\'', '\u{044D}'), // э
        ('z', '\u{044F}'), // я
        ('x', '\u{0447}'), // ч
        ('c', '\u{0441}'), // с
        ('v', '\u{043C}'), // м
        ('b', '\u{0438}'), // и
        ('n', '\u{0442}'), // т
        ('m', '\u{044C}'), // ь
        (',', '\u{0431}'), // б
        ('.', '\u{044E}'), // ю
        ('/', '.'),
        ('`', '\u{0451}'), // ё
        // Uppercase QWERTY -> Russian uppercase
        ('Q', '\u{0419}'), // Й
        ('W', '\u{0426}'), // Ц
        ('E', '\u{0423}'), // У
        ('R', '\u{041A}'), // К
        ('T', '\u{0415}'), // Е
        ('Y', '\u{041D}'), // Н
        ('U', '\u{0413}'), // Г
        ('I', '\u{0428}'), // Ш
        ('O', '\u{0429}'), // Щ
        ('P', '\u{0417}'), // З
        ('A', '\u{0424}'), // Ф
        ('S', '\u{042B}'), // Ы
        ('D', '\u{0412}'), // В
        ('F', '\u{0410}'), // А
        ('G', '\u{041F}'), // П
        ('H', '\u{0420}'), // Р
        ('J', '\u{041E}'), // О
        ('K', '\u{041B}'), // Л
        ('L', '\u{0414}'), // Д
        ('Z', '\u{042F}'), // Я
        ('X', '\u{0427}'), // Ч
        ('C', '\u{0421}'), // С
        ('V', '\u{041C}'), // М
        ('B', '\u{0418}'), // И
        ('N', '\u{0422}'), // Т
        ('M', '\u{042C}'), // Ь
        ('~', '\u{0401}'), // Ё
    ];

    KeyboardLayout::new("\u{0420}\u{0443}\u{0441}\u{0441}\u{043A}\u{0438}\u{0439}", "ru", mappings)
}
